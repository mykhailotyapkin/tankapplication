package tankapplication1;

/**
 *
 * @author Michael
 */
public class Bullets {
    
    private int size = 8;
    private int bulletX = 0;
    private int bulletY = 0;
    private double bulletVX = 1; 
    private double bulletVY = 1;

    /**
     * @return the size
     */
    public int getSize() {
        return size;
    }

    /**
     * @param size the size to set
     */
    public void setSize(int size) {
        this.size = size;
    }

    /**
     * @return the bulletX
     */
    public int getBulletX() {
        return bulletX;
    }

    /**
     * @param bulletX the bulletX to set
     */
    public void setBulletX(int bulletX) {
        this.bulletX = bulletX;
    }

    /**
     * @return the bulletY
     */
    public int getBulletY() {
        return bulletY;
    }

    /**
     * @param bulletY the bulletY to set
     */
    public void setBulletY(int bulletY) {
        this.bulletY = bulletY;
    }

    /**
     * @return the bulletVX
     */
    public double getBulletVX() {
        return bulletVX;
    }

    /**
     * @param bulletVX the bulletVX to set
     */
    public void setBulletVX(double bulletVX) {
        this.bulletVX = bulletVX;
    }

    /**
     * @return the bulletVY
     */
    public double getBulletVY() {
        return bulletVY;
    }

    /**
     * @param bulletVY the bulletVY to set
     */
    public void setBulletVY(double bulletVY) {
        this.bulletVY = bulletVY;
    }
    
    
}
